    private void btInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInsertActionPerformed
        // TODO add your handling code here:
        if(txtMaLop.getText().isEmpty() || txtTenLop.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nhap du thong t√≠n!", "Thong bao", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
            dsLop.clear();    
            try {
                quanLyLop qlLop = new quanLyLop();
                qlLop.docDanhSachLop(dsLop);
            } catch (SQLException ex) {
                Logger.getLogger(themLop.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            boolean check = false;
            for(Lop lop : this.dsLop){
                if(lop.getMaLop().equals(txtMaLop.getText())){
                    JOptionPane.showMessageDialog(this, "Ma Lop ton tai!", "Thong bao", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            
            Statement stmt = null;
            try (Connection conn = com.homework.services.JdbcUtils.getConn()){   
                    if(txtGVCN.getText().isEmpty()){
                        txtGVCN.setText("null");
                    }
                   
                    String sql = "INSERT INTO lop (MaLop,TenLop,GVCN) VALUES ('"+ txtMaLop.getText() + "','"+txtTenLop.getText() + "','"+ txtGVCN.getText() + "')";
                    stmt = (Statement) conn.createStatement();
                    stmt.executeUpdate(sql);
                    JOptionPane.showMessageDialog(this,sql,"Alter",JOptionPane.INFORMATION_MESSAGE);
                

            } catch (SQLException ex) {
                Logger.getLogger(themLop.class.getName()).log(Level.SEVERE, null, ex);
            }
                
        }
    }//GEN-LAST:event_btInsertActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        // TODO add your handling code here:
         dsLop.clear();  
         try {
                quanLyLop qlLop = new quanLyLop();
                qlLop.docDanhSachLop(dsLop);
            } catch (SQLException ex) {
                Logger.getLogger(themLop.class.getName()).log(Level.SEVERE, null, ex);
            }
         if(txtMaLop.getText().isEmpty() || txtTenLop.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nhap du thong t√≠n!", "Thong bao", JOptionPane.ERROR_MESSAGE);
         }
         else{
             for(Lop lop : this.dsLop){
                 if(!lop.getMaLop().toLowerCase().equals(txtMaLop)){
                      txtTenLop.setText("");
                      txtGVCN.setText("");
                     JOptionPane.showMessageDialog(this,"Ma Lop khong ton tai!","Thong bao",JOptionPane.INFORMATION_MESSAGE);
                     return;
                 }
             }
          try (Connection conn = com.homework.services.JdbcUtils.getConn()){
                String query = "update lop set TenLop ='"+txtTenLop.getText() + "',GVCN ='"+ txtGVCN.getText() + "' where MaLop ='" + txtMaLop.getText() +"'";
                Statement stmt = null;
                stmt = (Statement) conn.createStatement();
                stmt.execute(query);
                JOptionPane.showMessageDialog(this,"OK!","Thong bao",JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(themMonHoc.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,ex.toString(),"Thong bao",JOptionPane.INFORMATION_MESSAGE);
        }
         }
            
    }//GEN-LAST:event_btUpdateActionPerformed

    private void txtMaLopTextValueChanged(java.awt.event.TextEvent evt) {//GEN-FIRST:event_txtMaLopTextValueChanged
        // TODO add your handling code here:
        dsLop.clear();  
         try {
                quanLyLop qlLop = new quanLyLop();
                qlLop.docDanhSachLop(dsLop);
            } catch (SQLException ex) {
                Logger.getLogger(themLop.class.getName()).log(Level.SEVERE, null, ex);
            }
        for(Lop lop : this.dsLop){
            if(lop.getMaLop().toLowerCase().equals(txtMaLop.getText().toLowerCase())){
                txtTenLop.setText(lop.getTenLop());
                txtGVCN.setText(lop.getgVCN());
            }
            else if(txtMaLop.getText().isEmpty()){
                txtTenLop.setText("");
                txtGVCN.setText("");
            }
          
            
        }
    }//GEN-LAST:event_txtMaLopTextValueChanged

